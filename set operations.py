E={0, 2, 4, 6, 8}
N={1, 2, 3, 4, 5}

# Union
union=E.union(N)
print("the union is:",union)

# Intersection
intersection=E.intersection(N)
print("the intersection is:",intersection)

# Difference
diff=E.difference(N)
print("the difference is:",diff)

# Symmetric difference
symd=E.symmetric_difference(N)
print("the symmetric difference is:",symd)